char	*ft_strrev(char *str)
{
	int		i;
	int		n;
	char	stock;

	n = 0;
	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i = i - 1;
	while (n < i)
	{
		stock = str[n];
		str[n] = str[i];
		str[i] = stock;
		n++;
		i--;
	}
	return (str);
}
