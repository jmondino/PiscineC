int		ft_iterative_power(int nb, int power)
{
	int i;
	int nb2;

	nb2 = nb;
	i = 1;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (i < power)
	{
		nb2 = nb2 * nb;
		i++;
	}
	return (nb2);
}
